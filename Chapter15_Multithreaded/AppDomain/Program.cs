AppDomain چیست؟

AppDomain مثل یک محیط ایزوله (sandbox) است که برنامه‌ی .NET در آن اجرا می‌شود.

در نسخه‌های قدیمی .NET Framework، می‌توانستیم چند AppDomain بسازیم تا بخش‌های مختلف برنامه جدا باشند.
مثال: یک AppDomain برای برنامه‌ی اصلی، یک AppDomain برای یک افزونه (plugin) تا اگر افزونه خراب شد، برنامه اصلی تحت تاثیر قرار نگیرد.

در .NET Core دیگر نمی‌توان AppDomain جدید ساخت؛ فقط یک AppDomain داریم.

